#include "libft.h"

size_t				ft_nbrlen(int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < i)
		i += 1;
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);}

