#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *c;
	int i;

	c = s;
	if (!(*c))
		return;
	i = 0;
	while(i < (int)n)
	{
		c[i++] = '\0';
	}
}
