#include "libft.h"

int		ft_isalnum(int c)
{
	int count;

	count = 0;
	if ((c > 64 && c < 91) || (c > 96 && c < 123))	
		return (++count);
	else if (c > 47 && c < 58)
		return (++count);
	return (count);
}
